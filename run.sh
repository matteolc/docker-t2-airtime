#!/bin/bash
IMAGE=t2-airtime
echo "---------------------------------------------------------------------"
echo "Cleaning up"
echo "---------------------------------------------------------------------"
docker rm -f $IMAGE
echo "---------------------------------------------------------------------"
echo "Launching"
echo "---------------------------------------------------------------------"
docker run -d \
  --name ${IMAGE} \
  -p 5000:3000 \
  -e T2_SHOP_USER \
  -e T2_AIRTIME_KEY \
  -e CORS_ORIGINS \
  voxbox/${IMAGE}

